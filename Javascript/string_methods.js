//Different Methods of Strings in JS

// length Method
let favShow='Shark Tank India';
console.log(favShow.length);

console.log(favShow[favShow.length-1]);

// indexOf Method
console.log(favShow.indexOf('Tank'));

// slice Method
console.log(favShow.slice(6,10));

